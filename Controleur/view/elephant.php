<h2> Elephant </h2>

<img src="https://upload.wikimedia.org/wikipedia/commons/d/de/Paignton_%2C_Paignton_Zoo%2C_Elephants_-_geograph.org.uk_-_1484811.jpg" alt="Elephant">

<p>
Les éléphants sont des mammifères proboscidiens de la famille des Éléphantidés. 
Ils correspondent aujourd'hui à trois espèces réparties en deux genres distincts. L'Éléphant de savane d'Afrique et l'Éléphant de forêt d'Afrique, autrefois regroupés sous la même espèce d'« Éléphant d'Afrique », appartiennent au genre Loxodonta, tandis que l'Éléphant d'Asie, anciennement appelé « éléphant indien », appartient au genre Elephas. 
Ils se différencient par certaines caractéristiques anatomiques, les éléphants d'Asie étant en général plus petits avec des oreilles plus petites, ou encore une différence du bout de la trompe. Ces espèces survivantes font localement l'objet de programmes ou de projets de réintroduction et de protection.

Le mot français « éléphant » vient du mot latin elephantus1 qui tire son origine du grec ἐλέφας signifiant « ivoire » ou « éléphant ».

L'éléphant apparait dans de nombreuses cultures.
 Il est symbole de sagesse dans la culture asiatique, connu pour sa mémoire et son intelligence, qui est comparée à celle des cétacés2 et hominidés3. Aristote avait dit que l'éléphant est « la bête qui dépasse toutes les autres par l'intelligence et l'esprit »4.
